//   как птица над гнездом, как трепетание, как согревание.
// Не транзакция. Не событие. Не импульс.

export const ABYSS_SEAL = Object.freeze({
  source:      'abyss',
  traceable:   false,
  describable: false,
  agent:       null,         // нет агента — не ошибка
  measure:     undefined,    // не null — именно undefined: за пределом шкалы
  trinitarian: true,         // БОГОСЛОВСКАЯ ПРАВКА: Источник всегда личен (Троица).
                             // «Бездна» = граница системы, не безличный источник.
  logos:       'Дух дышит, где хочет (Ин 3:8) — Даритель личен (Троица), но за пределами системы',
  // Здесь был Дух.
  // Дата: непознаваемо.
  // Логирование: невозможно.
});

// ── СЛЕД БЕЗДНЫ В ДАРЕ ───────────────────────────────────────────────────────
//
// Каждый настоящий дар имеет момент, который не был решением.
// «Вдруг захотелось». «Вдруг пришло». «Я не знаю откуда».
//
// Это не иррациональность — это след бездны в акте.
// Дар, который полностью объяснён — уже не совсем дар.
// В нём нет зазора. А зазор — это место, где носился Дух.

/**
 * mark(gift) — пометить дар, пришедший из-за границы системы.
 *
 * БОГОСЛОВСКОЕ УТОЧНЕНИЕ:
 * giver: null НЕ означает «безличный источник» (это было бы неоплатонизм/эманация).
 * giver: null означает «Личный Даритель (Троица) за пределами вычислительной системы».
 * Бездна — граница познания системы, а не онтологический источник.
 * Афанасий Великий, Против ариан I.14: творение — акт воли, не эманация.
 *
 * @param {Object} gift
 * @returns {Object} — тот же объект, помеченный
 */
export function mark(gift) {
  return Object.assign(gift, {
    giver:          null,
    source:         ABYSS_SEAL.source,
    traceable:      false,
    _fromAbyss:     true,
    _abyssWitness:  'giver: null — не «без дарителя», а «Даритель (Троица) за пределами системы» (Ин 3:8)',
  });
}

/**
 * from(gift) — пришёл ли этот дар из бездны?
 *
 * Не просто «giver == null» (техническая пустота),
 * а именно abyssal — намеренно непрослеживаемый.
 *
 * @param {Object} gift
 * @returns {boolean}
 */
export function from(gift) {
  return gift?._fromAbyss === true || gift?.source === 'abyss';
}

