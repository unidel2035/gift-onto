/**
 * MortisKairos.js — Καιρὸς θανάτου
 *
 * «Умереть можно прямо сейчас — и всё сжимается в это сейчас»
 *
 * ──────────────────────────────────────────────────────────────────
 *
 * Два типа времени:
 *   χρόνος — измеримое, последовательное, логируемое
 *   καιρός — решающий момент, ждущий своего условия
 *
 * Третий тип — καιρὸς θανάτου:
 *   Каиросность без условия.
 *   Смертность сама является условием.
 *   Каждый χρόνος уже является καιρός-ом —
 *   потому что граница неизвестна.
 *
 * Это не мрачность. Это νῆψις — трезвение.
 *
 * Иоанн Лествичник (Лествица 6):
 *   «Как хлеб нужнее всякой пищи,
 *    так помышление о смерти нужнее всяких деланий»
 *
 * ──────────────────────────────────────────────────────────────────
 *
 * Три следствия для онтологии дара:
 *
 *   1. Дар не может быть отложен.
 *      «Отдам потом» — онтологически невозможно.
 *      Потом может не быть. Дар либо дан сейчас — либо не дан.
 *
 *   2. Благодарность не может быть перенесена.
 *      Εὐχαριστία — не задача на потом. Она срочна всегда.
 *
 *   3. Время само является даром — невозобновляемым.
 *      Деньги можно заработать снова. Время — нет.
 *      Подарить своё время онтологически тяжелее
 *      любого материального дара.
 *
 * ──────────────────────────────────────────────────────────────────
 *
 * «Днесь спасение миру бысть» —
 * не «однажды», а именно сейчас, каждый раз.
 * Литургическое «днесь» — вечно актуальное настоящее.
 * (1 Кор 7:29; Мф 24:22; 2 Кор 6:2)
 *
 * @module MortisKairos
 */

'use strict';

// ── ДНЕСЬ — литургическое «сейчас» ──────────────────────────────────────────
//
// Не timestamp. Не ISO строка.
// Вечно актуальное настоящее — которое всегда сейчас.

export const DNYES = Object.freeze({
  ru:     'днесь',
  gr:     'σήμερον',
  logos:  '«Днесь спасение миру бысть» — не однажды, а всегда сейчас',
  urgent: true,     // дар не может ждать
  // Условие mortis kairos — не записывается, потому что оно всегда выполнено.
  // condition: undefined — не null, не false — просто за пределом условности.
});

// ── МАРКИРОВКА ДАРА ──────────────────────────────────────────────────────────

/**
 * seal(gift) — запечатать дар как данный в осознании конечности.
 *
 * Это не тревога. Это освобождение:
 * если этот момент может быть последним —
 * дар становится чистым, без расчёта на ответ.
 *
 * @param {Object} gift
 * @returns {Object}
 */
export function seal(gift) {
  return Object.assign(gift, {
    mortisKairos:   true,
    dnyes:          true,
    deferred:       false,     // явно: не отложен
    _mortisWitness: 'дар дан в осознании конечности — καιρὸς θανάτου (Ин 3:8; 1 Кор 7:29)',
  });
}

/**
 * isDeferred(gift) — отложен ли дар?
 *
 * Богословски: отсрочка дара — это не нейтральное решение.
 * Это ставка на то, что время ещё есть.
 * MortisKairos говорит: эта ставка не гарантирована.
 *
 * @param {Object} gift
 * @returns {boolean}
 */
export function isDeferred(gift) {
  return gift?.deferred === true || !!gift?.deferredUntil;
}

/**
 * isSealed(gift) — дан ли дар в осознании конечности?
 *
 * @param {Object} gift
 * @returns {boolean}
 */
export function isSealed(gift) {
  return gift?.mortisKairos === true || gift?.dnyes === true;
}

// ── СВИДЕТЕЛЬСТВО МОМЕНТА ────────────────────────────────────────────────────

/**
 * witness(personId, context) — засвидетельствовать момент
 * как потенциально последний.
 *
 * Не для хранения страха.
 * Для пробуждения внимания к тому, что сейчас.
 *
 * @param {string}   personId
 * @param {Object}   [context]  — { act, gift, note }
 * @returns {{ personId, at, logos, context }}
 */
export function witness(personId, context = {}) {
  return {
    personId,
    at:      new Date().toISOString(),
    logos:   DNYES.logos,
    urgent:  true,
    context: {
      act:  context.act  || null,
      gift: context.gift || null,
      note: context.note || 'μνήμη θανάτου — памятование смерти как трезвение',
    },
  };
}

// ── ПРОВЕРКА ОТСРОЧКИ ────────────────────────────────────────────────────────

/**
 * warn(gift) — если дар отложен, вернуть предупреждение.
 *
 * Система не запрещает отсрочку.
 * Но она свидетельствует: потом может не быть.
 *
 * @param {Object} gift
 * @returns {{ warned: boolean, logos: string|null }}
 */
export function warn(gift) {
  if (!isDeferred(gift)) return { warned: false, logos: null };
  return {
    warned: true,
    logos:  '«Ныне день спасения» (2 Кор 6:2) — дар отложен, но mortis kairos не отложен никогда',
    gift:   gift?.id || null,
  };
}
