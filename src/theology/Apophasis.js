  /** Все апофатические границы */
  list() {
    return this.boundaries;
  }

  /** Получить границу по концепту */
  get(concept) {
    return this.boundaries.find(b =>
      b.concept.toLowerCase() === concept.toLowerCase()
    ) || null;
  }

  /** Проверить: система пытается вычислить невычислимое? */
  check(concept) {
    const boundary = this.get(concept);
    if (boundary) {
      return { computable: false, reason: boundary.text, source: boundary.source };
    }
    return { computable: true };
  }

  /**
   * guard(action, concept) — апофатический middleware.
   *
   * Выполняет действие, но сопровождает его свидетельством границы.
   * Система делает что может — и признаёт, чего не может.
   *
   * @param {function} action — действие (DivineEnergy.create и т.д.)
   * @param {string} concept — какую границу мы пересекаем
   * @param {Object} [logger] — куда логировать
   * @returns {*} результат action()
   */
  guard(action, concept, logger = null) {
    const boundary = this.get(concept);
    if (boundary && logger?.info) {
      logger.info(`[ἀπόφασις] ${concept}`);
    }
    const result = action();
    // Пометить результат — потребитель знает что это знак, не реальность
    if (result && typeof result === 'object' && boundary) {
      result._apophatic = boundary.text;
    }
    return result;
  }

