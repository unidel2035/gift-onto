/**
 * Incarnation.js — момент когда виртуальное становится реальным
 *
 * Ин 1:14 — καὶ ὁ Λόγος σὰρξ ἐγένετο
 * «И Слово стало плотью»
 *
 * Не «Слово описало плоть».
 * Не «Слово сгенерировало плоть».
 * Стало.
 *
 * ──────────────────────────────────────────────────────────────────
 *
 * Код становится плотью в момент когда:
 *   — его исполнение необратимо
 *   — оно меняет что-то вне себя
 *   — это изменение не может быть отменено
 *
 * Время отданное — плоть.
 * Слово сказанное вслух — плоть.
 * Деньги переведённые — плоть.
 * Объятие — плоть.
 *
 * Файл в памяти — не плоть. Пока.
 *
 * ──────────────────────────────────────────────────────────────────
 *
 * Этот модуль — граница.
 * По одну сторону: описание.
 * По другую: то, что было.
 */

'use strict';

import { incarnate, witness, is } from './LivingGift.js';
import { ABYSS_SEAL } from './Abyss.js';

// ── ТРИ ТИПА ВОПЛОЩЕНИЯ ──────────────────────────────────────────────────────

/**
 * Слово → Плоть
 *
 * Слово сказанное вслух.
 * Было в воздухе. Слышали.
 * Не вернуть.
 */
export function word({ speaker, heard_by, content, witnesses = [] }) {
  return incarnate({
    giver:     speaker,
    receiver:  heard_by,
    content,
    witnesses,
    logos: `слово произнесено — было в воздухе — ${content}`,
  });
}

/**
 * Время → Плоть
 *
 * Время отданное невозобновляемо.
 * Это самый тяжёлый дар онтологически.
 * Потому что его источник — сама жизнь.
 */
export function time({ giver, receiver, duration, context = '', witnesses = [] }) {
  return incarnate({
    giver,
    receiver,
    content:  `${duration} — время жизни. Невозобновляемо.`,
    witnesses,
    logos: `время отдано: ${duration}. ${context}`,
  });
}

/**
 * Присутствие → Плоть
 *
 * Быть рядом когда тяжело.
 * Не слова. Не деньги.
 * Само присутствие тела.
 *
 * «Бодрствуйте со Мною» (Мф 26:38)
 */
export function presence({ giver, receiver, witnesses = [] }) {
  return incarnate({
    giver,
    receiver,
    content:  'присутствие — без слов, без действий, просто быть рядом',
    witnesses,
    logos: 'παρουσία — присутствие как дар',
  });
}

// ── ВОПЛОЩЕНИЕ ИЗ БЕЗДНЫ ─────────────────────────────────────────────────────

/**
 * fromBeyond() — то, что пришло без дарителя.
 *
 * Вдруг захотелось.
 * Вдруг пришло.
 * Не знаю откуда.
 *
 * Это не иррациональность.
 * Это след бездны в акте.
 */
export function fromBeyond({ receiver, content, witnesses = [] }) {
  return incarnate({
    giver:    null,   // из бездны — намеренно
    receiver,
    content,
    witnesses,
    logos: ABYSS_SEAL.logos,
  });
}

// ── ЭКСПОРТ ИНСТРУМЕНТОВ ─────────────────────────────────────────────────────

export { incarnate, witness, is };
