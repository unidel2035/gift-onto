/**
 * Execute the Hexaemeron — create all days through divine energy.
 *
 * @param {object} engine — the GiftEngine
 * @param {Source} [source] — deprecated, ignored
 * @returns {object} — { days: Map<dayNum, giftId[]>, persons: Map<name, person> }
 */
export function executeHexaemeron(engine, source) {
  // Declare telos before creation
  engine.telos.declareTelos('новое основание', {
    description: 'Онтология Дара — система на основе самоотдания',
    vision: 'Мир, где каждый вычислительный акт — дар',
  });

  const dayGifts = new Map(); // dayNum → giftId[]
  const createdPersons = new Map(); // name → person

  // Resolve Logos (Son) — mediator of all creation (Ин 1:3)
  const logos = engine.persons.findByName('Λόγος');

  // Create each day
  for (const dayData of CREATION_DAYS) {
    // Resolve foundation gift IDs from day numbers
    const foundation = [];
    for (const fDay of dayData.foundationDays) {
      const dayGiftIds = dayGifts.get(fDay);
      if (dayGiftIds) foundation.push(...dayGiftIds);
    }

    const { person, gift } = DivineEnergy.create({
      name: dayData.name,
      calling: dayData.calling,
      description: dayData.description,
      logos: dayData.logos,
      telos: dayData.telos,
      day: dayData.day,
      ontologicalOrder: dayData.ontologicalOrder,
      foundation,
      mediator: logos ? logos.id : undefined,
    }, engine._personContext());

    if (!dayGifts.has(dayData.day)) dayGifts.set(dayData.day, []);
    dayGifts.get(dayData.day).push(gift.id);
    createdPersons.set(dayData.name, person);

    logger.info(`[Gift] День ${dayData.day}: ${dayData.name}`);
  }

  // Day 7: Rest — покой не принадлежит лицу, а всей системе
  engine.clock.enterContemplation('system', 'И увидел Бог всё, что Он создал, и вот, хорошо весьма');
  logger.info('[Gift] День 7: Покой — и вот, хорошо весьма');

  // Post-creation horizontal gifts
  let prevGiftId = null;
  for (const act of POST_CREATION) {
    if (act.type === 'offer') {
      const giverPerson = engine.persons.resolve(act.giver);
      if (!giverPerson) continue;

      const giftData = {
        giver: act.giver,
        receiver: act.receiver,
        content: act.content,
        logos: act.logos,
        cost: act.cost,
        telos: act.telos,
      };
      if (act.anamnesisFromPrev && prevGiftId) {
        giftData.anamnesis = [prevGiftId];
      }

      const g = engine.offer(giftData);
      prevGiftId = g.id;

      if (act.accept && g.id) {
        engine.accept(g.id, act.accept);
      }
    } else if (act.type === 'eucharistia') {
      const person = engine.persons.resolve(act.person);
      if (person) {
        engine.eucharistia(person.id, act.content);
      }
    }
  }

  return { dayGifts, createdPersons };
}

export default { CREATION_DAYS, POST_CREATION, executeHexaemeron };
